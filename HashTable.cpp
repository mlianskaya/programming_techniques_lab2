/**
 * @file HashTable.cpp
 * @brief Реализация хэш-таблицы для хранения абитуриентов.
 * @details Содержит методы:
 *          - hash() – полиномиальная хэш-функция с приведением к нижнему регистру;
 *          - insert() – вставка элемента с учётом дубликатов и подсчётом коллизий;
 *          - find() – поиск всех элементов по ключу;
 *          - clear() – очистка таблицы и сброс счётчика коллизий.
 */

#include "HashTable.hpp"
#include <cctype>   // для std::tolower

// Конструктор / Деструктор 

/**
 * @brief Конструктор хэш-таблицы.
 * @param initial_size Начальный размер таблицы (количество корзин).
 *        Рекомендуется выбирать простое число для равномерного распределения.
 */
HashTable::HashTable(size_t initial_size) : table(initial_size), collisions(0) {}

/** @brief Деструктор. Ресурсы освобождаются автоматически (std::unordered_map очищается). */
HashTable::~HashTable() {}

// Хэш-функция 

/**
 * @brief Полиномиальная хэш-функция для строки (ФИО).
 * @param key Входная строка.
 * @return Хэш-код в диапазоне [0, table.size()-1].
 * @details
 * - Используется простое число p = 131.
 * - Формула: hash = (c0 + c1*p + c2*p^2 + ...) % mod, но с модулем на каждом шаге.
 * - Символы приводятся к нижнему регистру (для регистронезависимости).
 * - Для букв a-z используется значение 1..26, для остальных символов (пробел) – 27.
 * - Финальное значение берётся по модулю размера таблицы.
 * @note Хэш-функция хорошо распределяет строки, но не может устранить коллизии,
 *       вызванные многократными дубликатами одного ключа.
 */
size_t HashTable::hash(const std::string& key) const {
    const size_t p = 131;               // простое число
    const size_t mod = table.size();    // размер таблицы (простое число)
    size_t hash = 0;
    for (char ch : key) {
        // Приводим символ к нижнему регистру
        unsigned char lower = std::tolower(static_cast<unsigned char>(ch));
        // Преобразуем букву в число 1..26, небуквенные символы -> 27
        size_t val = (lower >= 'a' && lower <= 'z') ? (lower - 'a' + 1) : 27;
        // Полиномиальный хэш с модулем на каждом шаге (предотвращает переполнение)
        hash = (hash * p + val) % mod;
    }
    return hash;
}

// Вставка 

/**
 * @brief Вставка абитуриента в хэш-таблицу.
 * @param a Объект Applicant.
 * @details
 * - Вычисляется индекс корзины с помощью hash().
 * - Если ключ (ФИО) уже существует в корзине, объект добавляется в существующий вектор.
 * - Если ключ отсутствует, создаётся новая запись (ключ → вектор с одним элементом).
 * - Счётчик коллизий увеличивается только когда в непустую корзину добавляется
 *   новый уникальный ключ (конфликт разных ключей). Дубликаты не считаются коллизиями.
 */
void HashTable::insert(const Applicant& a) {
    size_t idx = hash(a.fullName);
    auto& bucket = table[idx];
    auto it = bucket.find(a.fullName);
    if (it == bucket.end()) {
        // Ключ отсутствует в этой корзине – создаём новую запись
        bucket.emplace(a.fullName, std::vector<Applicant>{a});
        // Если корзина уже содержала другие ключи, это коллизия (разные ключи)
        if (bucket.size() > 1) collisions++;
    } else {
        // Ключ уже есть – добавляем в существующий вектор (дубликат)
        it->second.push_back(a);
        // Коллизия не увеличивается, так как это дубликат, а не конфликт разных ключей
    }
}

// Поиск 

/**
 * @brief Поиск всех абитуриентов по ключу (ФИО).
 * @param key Искомое ФИО.
 * @return Вектор копий найденных объектов. Если ключ не найден, возвращается пустой вектор.
 * @details
 * - Вычисляется хэш, находится корзина.
 * - В корзине выполняется поиск по ключу (за O(1) в среднем).
 * - Если ключ найден, возвращается вектор значений; иначе – пустой вектор.
 * - Сложность в среднем O(1), в худшем O(k), где k – число различных ключей в корзине.
 */
std::vector<Applicant> HashTable::find(const std::string& key) const {
    size_t idx = hash(key);
    const auto& bucket = table[idx];
    auto it = bucket.find(key);
    if (it != bucket.end()) {
        return it->second;   // возвращаем вектор значений
    }
    return {};
}

// Очистка

/**
 * @brief Очистка хэш-таблицы.
 * @details Удаляет все элементы из всех корзин и сбрасывает счётчик коллизий.
 *          Размер таблицы остаётся неизменным.
 */
void HashTable::clear() {
    for (auto& bucket : table) bucket.clear();
    collisions = 0;
}