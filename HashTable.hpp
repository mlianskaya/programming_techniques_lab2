/**
 * @file HashTable.hpp
 * @brief Заголовочный файл хэш-таблицы для хранения абитуриентов.
 * @details Реализует хэш-таблицу с методом цепочек, где каждая корзина
 *          представляет собой отображение (std::unordered_map) из ключа
 *          (ФИО) в вектор значений. Это позволяет эффективно хранить
 *          дубликаты и избегать линейного перебора при поиске.
 */

#pragma once
#include <vector>
#include <unordered_map>
#include <string>
#include "Applicant.hpp"

/**
 * @class HashTable
 * @brief Класс хэш-таблицы для быстрого поиска по ключу.
 * @details
 * - Размер таблицы фиксирован и задаётся в конструкторе (рекомендуется простое число).
 * - Коллизии разрешаются методом цепочек: каждая корзина — это std::unordered_map.
 * - Внутри корзины ключи уникальны, а для каждого ключа хранится вектор всех значений.
 * - Дубликаты (одинаковые ФИО) не создают коллизий, а добавляются в существующий вектор.
 * - Ведётся счётчик коллизий (сколько раз при вставке нового уникального ключа
 *   корзина уже была непуста).
 */
class HashTable {
public:
    /**
     * @brief Конструктор хэш-таблицы.
     * @param initial_size Начальный размер таблицы (количество корзин).
     *                     Рекомендуется выбирать простое число для равномерного распределения.
     */
    explicit HashTable(size_t initial_size = 10007);

    /** @brief Деструктор. Освобождает ресурсы (списки очищаются автоматически). */
    ~HashTable();

    /**
     * @brief Вставка абитуриента в таблицу.
     * @param a Объект Applicant.
     * @details
     * - Вычисляется хэш от fullName, определяется корзина.
     * - Если ключ уже существует в корзине, объект добавляется в соответствующий вектор.
     * - Если ключ отсутствует, создаётся новая запись (ключ → вектор с одним элементом)
     *   и увеличивается счётчик коллизий, если корзина уже была непуста.
     * - Дубликаты не увеличивают счётчик коллизий.
     */
    void insert(const Applicant& a);

    /**
     * @brief Поиск всех абитуриентов по ключу (ФИО).
     * @param key Искомое ФИО.
     * @return Вектор копий найденных объектов. Если ключ не найден, возвращается пустой вектор.
     * @details
     * - Вычисляется хэш, находится корзина.
     * - В корзине выполняется поиск по ключу (за O(1) в среднем).
     * - Если ключ найден, возвращается вектор значений; иначе – пустой вектор.
     * - Сложность в среднем O(1), в худшем O(k), где k – число различных ключей в корзине.
     */
    std::vector<Applicant> find(const std::string& key) const;

    /**
     * @brief Возвращает общее количество коллизий.
     * @return Количество случаев, когда при вставке нового уникального ключа
     *         корзина уже была непустой.
     */
    size_t getCollisions() const { return collisions; }

    /**
     * @brief Очищает таблицу (удаляет все элементы) и сбрасывает счётчик коллизий.
     * @details Размер таблицы не изменяется.
     */
    void clear();

private:
    /**
     * @brief Хэш-функция для строки.
     * @param key Входная строка (ФИО).
     * @return Хэш-код в диапазоне [0, table.size()-1].
     * @details Используется полиномиальная хэш-функция с простым множителем 131.
     *          Символы приводятся к нижнему регистру (для регистронезависимости).
     *          Небуквенные символы (например, пробел) кодируются числом 27.
     *          Результат берётся по модулю размера таблицы.
     * @note Функция не гарантирует уникальность хэшей для разных ключей,
     *       но обеспечивает равномерное распределение.
     */
    size_t hash(const std::string& key) const;

    /**
     * @brief Массив корзин.
     * @details Каждая корзина – это отображение из строки (ключ) в вектор значений.
     *          Такая структура позволяет хранить дубликаты без увеличения времени поиска.
     */
    std::vector<std::unordered_map<std::string, std::vector<Applicant>>> table;

    size_t collisions; ///< Счётчик коллизий (конфликтов разных ключей).
};